#!/bin/bash

# Цвета для вывода
GREEN='\033[0;32m'
RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}Запуск скрипта исправления Колеса Баланса${NC}"

# 1. Остановка всех процессов на порту 5000
echo -e "${BLUE}Останавливаем все процессы на порту 5000...${NC}"
npx kill-port 5000

# 2. Заполняем базу данных тестовыми данными колеса баланса
echo -e "${BLUE}Заполнение базы данных тестовыми данными...${NC}"
npm run seed

if [ $? -eq 0 ]; then
  echo -e "${GREEN}Данные успешно добавлены в базу данных${NC}"
else
  echo -e "${RED}Ошибка при добавлении данных в базу данных${NC}"
  exit 1
fi

# 3. Запускаем приложение
echo -e "${BLUE}Запуск приложения...${NC}"
npm run dev

echo -e "${GREEN}Скрипт завершен. Проверьте работу Колеса Баланса в браузере.${NC}" 